import dayjs from "dayjs"
import "./Orders.css";
import {Link} from "react-router-dom"
import Header from  "./Header.jsx"
import { useState,useEffect, Fragment } from "react";




export default function Orders({cart,products}){


    const [orders,setOrders]=useState([])
    useEffect(()=>{
        fetch("http://localhost:3000/api/orders").then((response)=>{
            return response.json();
        }).then((data)=>{
            setOrders(data);
        })
    },[])



    return(
        <>
            <Header cart={cart}/>
            <div className="orders-page">
            <div className="page-title">Your Orders</div>

            <div className="orders-grid">
                {orders.map((order)=>{ 
                return(
                    <div className="order-container" key={order.id}>
                    <div className="order-header">
                        <div className="order-header-left-section">
                        <div className="order-date">
                            <div className="order-header-label">Order Placed:</div>
                            <div>August 12</div>
                        </div>
                        <div className="order-total">
                            <div className="order-header-label">Total:</div>
                            <div>{(order.totalCostCents/100).toFixed(2)}</div>
                        </div>
                        </div>

                        <div className="order-header-right-section">
                        <div className="order-header-label">Order ID:</div>
                        <div>{order.id}</div>
                        </div>
                    </div>

                    <div className="order-details-grid">
                        {order.products.map((prod)=>{
                            let sele=''
                            products.forEach((product)=>{
                                if(product.id===prod.productId){
                                    sele=product;
                                }
                            })
                        return(
                            <Fragment key={prod.productId}>
                                <div className="product-image-container">
                                <img src={sele.image} />
                                </div>

                                <div className="product-details">
                                <div className="product-name">
                                    {sele.name}
                                </div>
                                <div className="product-delivery-date">
                                    Arriving on: {dayjs(sele.estimatedDeliveryTimeMs).format('dddd MMMM D')}
                                </div>
                                <div className="product-quantity">
                                    Quantity: {prod.quantity}
                                </div>
                                <button className="buy-again-button button-primary">
                                    <img className="buy-again-icon" src="images/icons/buy-again.png" />
                                    <span className="buy-again-message">Add to Cart</span>
                                </button>
                                </div>

                                <div className="product-actions">
                                <Link to="tracking.html">
                                    <button className="track-package-button button-secondary">
                                    Track package
                                    </button>
                                </Link>
                                </div>
                            </Fragment>
                        )
                        })}
                    </div>
                    </div>
                )
                })}
            </div>
            </div>
        </>
    )
}